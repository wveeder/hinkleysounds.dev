					<div class="about_txt">
						<br />
						<br />
						<p>Since the early 2000's, the band Hinkley has been floating somewhere above the urban industrial wasteland that is
						Rochester, NY, sending down its own brand of cracked country music. Blending its influences of Crazy Horse and The Band with Indie-Rock Shoegaze bliss drones and walls of fuzz & rumble, they create an idiosyncratic beauty that's not afraid to bare its bruises.</p>
						<br />
						<br />
						<br />
						<p>Front man Will Veeder's vision and vulnerable vocals are filtered thru the greasy lens made up of veteran string benders Jona Toll on bass & John Depuy on guitar. All anchored by the swing & clatter of percussionist Charles Leport.</p>
						<br />
						<br />
						<br />
						<p>Despite accomplishmemts that includ a live set aired on public television program <a href="http://interactive.wxxi.org/onstage">OnStage</a> and having songs featured in independent movies
						<a href="http://www.adamimitatingart.com/">Adam Imitating Art (2015 Raindance film festival selection)</a> and
						 <a href="http://eggwork.com/">Smoking Laws (Eggworks production)</a>, Hinkley's back catolog of 4 LPs and 3 EPs is largely an untapped artery waiting to be discovered.</p>
						<br />
						<br />
						<br />
						<p>Having slogged through the haunted ballrooms Hinkley has finally arrived at the post everything now, with their new release Peak of Light. Peak of Light warrants multiple spins to pick up on the subtleties swimming beneath the main body of the music. There are moments when the sound hovers in the air and makes you breathe funny. From the retina rearranging cover art to the deep pockets of rock spilling from the vinyl grooves Peak of Light is a mature yet still reckless record, and at all times totally engaging. For fans of Alt-Country,Indie-Rock and Shoegaze stretch outs.</p>

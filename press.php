			<p>Since the early 2000's, the band Hinkley has been floating somewhere above the urban industrial wasteland that is
			Rochester, NY, sending down its own brand of cracked country music. Blending its influences of Crazy Horse and The Band with Indie-Rock Shoegaze bliss drones and walls of fuzz & rumble, they create an idiosyncratic beauty that's not afraid to bare its bruises.</p>
			<br />
			<br />
			<br />
						<br />
			<h5>Scores of other bands play a loosey-goosey indie rock sound with a patina of country feedback, and fail where Hinkley succeeds - Culture Bunker </h5>
			<br />
			<br />
			<br />
			<br />
			<br />
			<p>Front man Will Veeder's vision and vulnerable vocals are filtered thru the greasy lens made up of veteran string benders Jona Toll on bass & John Depuy on guitar. All anchored by the swing & clatter of percussionist Charles Leport.
			</p>

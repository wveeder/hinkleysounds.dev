			<p>
			Having slogged through the haunted ballrooms Hinkley has finally arrived at the post everything now, with their new release Peak of Light. Peak of Light warrants multiple spins to pick up on the subtleties swimming beneath the main body of the music. There are moments when the sound hovers in the air and makes you breathe funny. From the retina rearranging cover art to the deep pockets of rock spilling from the vinyl grooves Peak of Light is a mature yet still reckless record, and at all times totally engaging. For fans of Alt-Country,Indie-Rock and Shoegaze stretch outs</p>

